"""When2Heat cross-validation of the published Germany 2010 dataset.

Paper section: Validation -> Tier 4 (When2Heat aggregate cross-validation).

Compares the temporal shape of our pipeline's national-aggregate
residential heating profile against When2Heat (Ruhnau et al., 2019,
Open Power System Data) — the established European single-RC heat-
demand benchmark. The comparison fills a validation gap that the NREL
EULP-based Tier 1A / Tier 3 tests do not: those validate the
*methodology* under matched ResStock inputs; this validates that the
*actual numbers in the published Germany 2010 parquets* are
consistent with an independent German benchmark.

Scope and limits

  * Heating only. When2Heat does not publish cooling profiles (German
    residential cooling has historically been negligible).
  * Shape-only comparison: both series are normalised before comparing.
    Reasons:
      - our coverage is SFH only; When2Heat covers all residential
        (SFH + MFH + apartments)
      - we do not apply census-equivalent stock-mix weights across
        the 3.3 M building-year combinations
    Absolute MW comparison would require those weights and would
    conflate stock-mix bias with methodology bias. Normalised shape
    isolates the methodology comparison cleanly.
  * The methodologies differ by design: When2Heat = single-RC × HDD ×
    national floor area; ours = 4045 locations × 11 archetypes ×
    74 profiles × hourly RC simulation. A close shape match
    demonstrates that our methodology preserves the temporal heat-
    demand dynamics established in the literature *while* delivering
    per-household variability that single-RC approaches cannot.

Inputs

  * ``output/HC/locXXXX/hc_archYY.parquet`` — pipeline outputs
    (4045 locations × 11 archetypes per location, 74 profiles per file).
    Generated by ``uv run coupled-energy-ts run config/germany_2010.yml``.
  * When2Heat CSV — download from
        https://data.open-power-system-data.org/when2heat/
    Save the CSV (e.g. ``when2heat.csv``) under
    ``validation/germany/data/when2heat.csv``. Pass with
    ``--when2heat-csv`` if elsewhere.

Outputs

  * ``img/de_demand_comparison.png`` and ``.pdf`` — 2-panel figure:
      Panel A: full-year daily-mean overlay (normalised to peak)
      Panel B: peak-week (January) hourly overlay (normalised to peak)
  * ``validation/germany/data/when2heat_metrics.csv`` — Pearson r
    (hourly + daily-mean), normalised RMSE, peak-day correlation.

Usage

    uv run python validation/germany/demand_comparison.py \\
        --when2heat-csv validation/germany/data/when2heat.csv

    # If your HC outputs are elsewhere:
    uv run python validation/germany/demand_comparison.py \\
        --when2heat-csv .../when2heat.csv \\
        --hc-root /path/to/output/HC
"""
